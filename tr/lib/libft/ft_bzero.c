#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	char			*str;

	if (s == 0)
		return ;
	i = 0;
	str = (char*)s;
	while (i < n)
	{
		*str++ = 0;
		i++;
	}
}
